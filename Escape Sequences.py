# Escape sequences are used to include special characters in strings.
# Common escape sequences:
# \n: Newline
# \t: Tab
# \\: Backslash
# \": Double quote
# \': Single quote

# Example:
print("Hello\nWorld!")
print("This is a tab\tcharacter.")

print("Sarvesh is here :\\Food\\Drink")

print('It\'s a sunny day.')

print("I said, \"Hello!\"")