# Q. Create a dictionary of three friends and their phone numbers. Use:

# keys() to get all names
# values() to get all numbers
# items() to loop over key-value pairs and print them


Diary = { "sarvesh" : "010120001",
 "Rohan" : "020220002",
  "Saksham" : "030320003"}

print(Diary)

Diary.keys()   # Returns a list of all the keys in the dictionary
print(Diary.keys())

Diary.values()  # Returns a list of all the values in the dictionary
print(Diary.values())

Diary.items()   # Returns a list of tuples containing key-value pairs
print(Diary.items())
