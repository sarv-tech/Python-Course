# Q. Create a program that checks if a person is eligible to vote or not (age >= 18).

age =  int(input("Enter your age: "))

if age >= 18:
    print("You are eligible to vote.")
else:
    print("You are not eligible to vote.")

# age >= 18 indicates age is greater than or equal to 18 so, person is eligible to vote.
# age < 18 indicates age is less than 18 so, As per rule, person is not eligible to vote.