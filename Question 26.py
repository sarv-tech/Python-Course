# Q. Given sentence = "Coding in Python is fun", replace "fun" with "awesome" and print it.
# Find the index of the word "Python" in sentence.
# Convert the entire sentence to uppercase and print it.

sentence = "Coding in Python is fun"

result = sentence.replace("fun", "awesome")
print(result)
output = sentence.upper()
print(output)
print(sentence.index("Python")) 
